# Пример 1: Простейшая программа
print("Hello, World!")  # Вывод текста в консоль

# Пример 2: Использование отступов
if True:
    print("Этот код находится внутри блока if!")  # Отступы обязательны

# Пример 3: Ввод данных от пользователя
name = input("Введите ваше имя: ")  # Функция input() возвращает строку
print(f"Привет, {name}!")  # Использование f-строки для форматирования

# Пример 4: Преобразование ввода в число
try:
    age = int(input("Введите ваш возраст: "))  # Преобразуем строку в целое число
    print(f"Через 10 лет вам будет {age + 10} лет.")
except ValueError:
    print("Ошибка: вы ввели не число!")  # Обработка ошибки, если введено не число

# Пример 5: Использование print() с несколькими аргументами
print("Имя:", name, "Возраст:", age)  # Вывод нескольких значений
print("Имя:", name, "Возраст:", age, sep=" | ")  # Изменение разделителя

