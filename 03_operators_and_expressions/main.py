# 03_operators_and_expressions/main.py

# Пример 1: Арифметические операторы
x = 10
y = 3
print("x + y =", x + y)  # Сложение
print("x - y =", x - y)  # Вычитание
print("x * y =", x * y)  # Умножение
print("x / y =", x / y)  # Деление
print("x // y =", x // y)  # Целочисленное деление
print("x % y =", x % y)  # Остаток от деления
print("x ** y =", x ** y)  # Возведение в степень

# Пример 2: Операторы сравнения
print("x > y =", x > y)  # Больше
print("x < y =", x < y)  # Меньше
print("x == y =", x == y)  # Равно
print("x != y =", x != y)  # Не равно

# Пример 3: Логические операторы
a = True
b = False
print("a and b =", a and b)  # Логическое И
print("a or b =", a or b)  # Логическое ИЛИ
print("not a =", not a)  # Логическое НЕ

# Пример 4: Приоритет операторов
result = 10 + 5 * 2  # Умножение выполняется первым
print("10 + 5 * 2 =", result)

